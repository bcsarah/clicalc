// Função para Somar
export function somar(a: number, b: number): number {
    return a + b
}

// Função para Diminuir
export function diminuir(a: number, b: number): number {
    return a - b
}

// Função para Multiplicar
export function multiplicar(a: number, b: number): number {
    return a * b
}

// Função para Diminuir
export function dividir(a: number, b: number): number {
    return a / b
}

// Função para Expoenciar
export function expoenciar(a: number, b: number): number {
    return a ** b
}

// Função para Radiciar
export function radiciar(a: number, b: number): number {
    return a ** (1 / b)
}
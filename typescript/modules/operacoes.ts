// Funções p/ Calculos Matemáticos
export let somar = (a: number, b: number): number => { return a + b; }
export let diminuir = (a: number, b: number): number => { return a - b; }
export let multiplicar = (a: number, b: number): number => { return a * b; }
export let dividir = (a: number, b: number): number => { return a / b; }
export let expoenciar = (a: number, b: number): number => { return a ** b; }
export let radiciar = (a: number, b: number): number => { return a ** (1 / b); }
export let restoDivisao = (a: number, b: number): number => { return a % b; }
// Operações Matemáticas
public class Operacoes {
    public static double somar(double a, double b) { return a + b; }
    public static double diminuir(double a, double b) { return a - b; }
    public static double multiplicar(double a, double b) { return a * b; }
    public static double dividir(double a, double b) { return a / b; }
    public static double expoenciar(double a, double b) { return Math.pow(a, b); }
    public static double radiciar(double a, double b) { return Math.pow(a, (1/b)); }
    public static double restoDivisao(double a, double b) { return a % b; }
}

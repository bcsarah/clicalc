# Funções de Operações Matemáticas
def somar(a: float, b: float) -> float: return a + b
def diminuir(a: float, b: float) -> float: return a - b
def multiplicar(a: float, b: float) -> float: return a * b
def dividir(a: float, b: float) -> float: return a / b
def expoenciar(a: float, b: float) -> float: return a ** b
def radiciar(a: float, b: float) -> float: return a ** (1 / b)
def restoDivisao(a: float, b: float) -> float: return a % b
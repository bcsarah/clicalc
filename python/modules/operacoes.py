# Função para Somar
def somar(a: float, b: float) -> float:
    return a + b

# Função para Diminuir
def diminuir(a: float, b: float) -> float:
    return a - b

# Função para Multiplicar
def multiplicar(a: float, b: float) -> float:
    return a * b

# Função para Dividir
def dividir(a: float, b: float) -> float:
    return a / b

# Função para Expoenciar
def expoenciar(a: float, b: float) -> float:
    return a ** b

# Função para Radiciar
def radiciar(a: float, b: float) -> float:
    return a ** (1 / b)

# Função para calcular Resto da Divisão
def restoDivisão(a: float, b: float) -> float:
    return a % b